public class RectangleBadConstructors {

    private int x;
    private int y;
    private int width;
    private int height;

    public RectangleBadConstructors() {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public RectangleBadConstructors(int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public RectangleBadConstructors(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

}
